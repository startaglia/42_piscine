#include<unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	temp = 0;
	i = 0;
	while (i <= size)
	{
		j = i;
		while (j < size)
		{
			if (tab [j] > tab[j + 1])
			{
				temp = tab[j + 1];
				tab[j + 1] = tab[j];
				tab[j] = temp;
			}
			j++;
		}
		size --;
	}
}
